void solve(){

	int t,b1[26],b2[26];

	bool k1,k2;

	string s1,s2;

	cin>>t;

	while(t--){

		k1=k2=0;

		cin>>s1>>s2;

		for(int i=0;i<26;i++){

			b1[i]=b2[i]=0;
		}

		for(int i=0;i<s1.length();i++)b1[s1[i]-'a']++;

		for(int i=0;i<s2.length();i++)b2[s2[i]-'a']++;

		for(int i=0;i<26;i++)if((b1[i]&&(!b2[i]))||(b2[i]&&(!b1[i])))k1=1;

		if(!k1){

			for(int i=0;i<26;i++){

				if(b1[i]!=b2[i])k2=1;
			}
		}

		if(k1==0&&k2==1)cout<<"NO\n";

		else cout<<"YES\n";
	}
	
}

