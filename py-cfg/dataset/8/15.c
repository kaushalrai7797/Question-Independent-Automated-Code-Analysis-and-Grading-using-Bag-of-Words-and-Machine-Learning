#include<stdio.h>
int main()
{
	int a[10000],i,s,j,t,k=0,f,l,n,o,b[10000],c[10000],z;
	scanf("%d",&s);
	for(i=0;i<s;i++)
	{
		int sum1=0,sum2=0,m=0;
		scanf("%d%d",&n,&o);
		for(j=0;j<n;j++)
		scanf("%d",&a[j]);
		k=0;
		f=0;
		for(j=0;j<n;j++)
        {
			if(j%2==0)
		    {
			  b[k]=a[j];
			  k++;
			}
		    else
			{
			  c[f]=a[j];
			  f++;
			}
		}
		for(j=0;j<k;j++)
		{
			for(l=0;l<k;l++)
			{
				if(b[j]<b[l])
				{
					t=b[j];
					b[j]=b[l];
					b[l]=t;
				}
			}
		}
		for(j=0;j<f;j++)
		{
			for(l=0;l<f;l++)
			{
				if(c[j]<c[l])
				{
					t=c[l];
					c[l]=c[j];
					c[j]=t;
				}
			}
		}
		for(m=0;m<o;m++)
		{
			if(c[m]<b[k-1-m])
			{
			  t=c[m];
			  c[m]=b[k-1-m];
			  b[k-1-m]=t;
		    }
		}
		for(j=0;j<k;j++)
		{
			sum1=sum1+b[j];
		}
		for(j=0;j<f;j++)
		{
			sum2=sum2+c[j];
		}
		if(sum2>sum1)
		printf("YES");
		else
		printf("NO");
	}
	return(0);
}